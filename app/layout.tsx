"use client";

// import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { useAccount, WagmiProvider, type State } from "wagmi";
import { config } from "@/config/wagmi";
import { Account } from "@/wagmi/account";
import { WalletOptions } from "@/wagmi/wallet-options";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

// export const metadata: Metadata = {
//   title: "Create Next App",
//   description: "Generated by create next app",
// };

const queryClient = new QueryClient();

function ConnectWallet() {
  const { isConnected } = useAccount();
  if (isConnected) return <Account />;
  return <WalletOptions />;
}

type Props = {
  children: React.ReactNode;
  initialState: State | undefined;
};

export default function RootLayout({ children, initialState }: Props) {
  return (
    <WagmiProvider config={config} initialState={initialState}>
      <QueryClientProvider client={queryClient}>
        <html lang="en">
          <body
            className={`${geistSans.variable} ${geistMono.variable} dark mt-10 mb-20 antialiased`}
          >
            <ConnectWallet />
            {children}
          </body>
        </html>
      </QueryClientProvider>
    </WagmiProvider>
  );
}
